


x = -4
x_abs = abs(x)
print(x_abs)

y = 2.5
y_abs = abs(y)
print(y_abs)

'''
| Type      | Example      | Result                        |
| --------- | ------------ | ----------------------------- |
| `int`     | `abs(-5)`    | `5`                           |
| `float`   | `abs(-3.14)` | `3.14`                        |
| `complex` | `abs(3+4j)`  | `5.0` (returns the magnitude) |
'''

print(abs(3 + 4j))  # 5.0
